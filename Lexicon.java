
public class Lexicon {

	public String From_Name = "";
	public String To_Name = "";
	
	public String Type_Target = "";
	public String Type_Extra = "";
		
	Lexicon(String F_Name, String T_Name, String T_Target, String T_Extra){		
		From_Name = F_Name;
		To_Name = T_Name;
		Type_Target = T_Target;
		Type_Extra = T_Extra;		
	}
	
}
